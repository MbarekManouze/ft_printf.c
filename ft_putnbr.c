#include "ft_printf.h"

void	ft_putnchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(long int n)
{
	int	x;

	x = 1;
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			ft_putnchar('-');
			n *= -1;
		}
		while (x <= (n / 10))
		{
			x *= 10;
		}
		while (x > 0)
		{
			ft_putnchar(((n / x) + '0'));
			n = n % x;
			x /= 10;
		}
	}
}
