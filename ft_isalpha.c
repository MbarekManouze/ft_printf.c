#include "ft_printf.h"

int	ft_isalpha(int c)
{
	if (c == 'd' || c == '%' || c == 'c' || c == 'i'
		|| c == 'x' || c == 'X' || c == 's' || c == 'p' || c == 'u')
		return (1);
	else
		return (0);
}
